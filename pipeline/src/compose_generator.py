class ComposeGenerator:
    def __init__(self, output_root: Path, package_name: str = "com.example.generated") -> None:
        self.output_root = output_root
        self.package_name = package_name
        self.screens_dir = output_root / "screens"
        self.components_dir = output_root / "components"
        self.previews_dir = output_root / "previews"
        self.logger = logging.getLogger("pipeline.compose")
        self.screens_dir.mkdir(parents=True, exist_ok=True)
        self.components_dir.mkdir(parents=True, exist_ok=True)
        self.previews_dir.mkdir(parents=True, exist_ok=True)

    def generate(self, dsl: dict[str, Any]) -> list[Path]:
        generated: list[Path] = []
        screens = dsl.get("screens", [])
        self.logger.info("Compose generation started: screens=%s", len(screens))
        for screen in dsl["screens"]:
            screen_name = _pascal(screen["name"])
            screen_path = self.screens_dir / f"{screen_name}Screen.kt"
            preview_path = self.previews_dir / f"{screen_name}Preview.kt"
            screen_path.write_text(self._build_screen_file(screen_name, screen), encoding="utf-8")
            preview_path.write_text(self._build_preview_file(screen_name), encoding="utf-8")
            generated.extend([screen_path, preview_path])
            self.logger.info("Generated screen pair: %s", screen_name)
        self.logger.info("Compose generation finished: files=%s", len(generated))
        return generated

