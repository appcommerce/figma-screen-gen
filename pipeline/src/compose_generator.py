def _pascal(value: str) -> str:
    chunks = [x for x in value.replace("_", " ").split() if x]
    if not chunks:
        return "Unknown"
    return "".join(ch[:1].upper() + ch[1:] for ch in chunks)


def _escape(value: str) -> str:
    return value.replace("\\", "\\\\").replace("\"", "\\\"")


def _safe_res_name(value: str) -> str:
    out = []
    for ch in value:
        if ch.isalnum() or ch == "_":
            out.append(ch.lower())
        else:
            out.append("_")
    normalized = "".join(out).strip("_")
    if not normalized:
        return "placeholder"
    if normalized[0].isdigit():
        return f"res_{normalized}"
    return normalized
