    def _build_modifier(self, props: dict[str, Any]) -> str:
        chain: list[str] = ["Modifier"]
        width = props.get("widthDp")
        height = props.get("heightDp")
        if width and height:
            chain.append(f".width({width}.dp).height({height}.dp)")
        elif width:
            chain.append(f".width({width}.dp)")
        elif height:
            chain.append(f".height({height}.dp)")
        padding = props.get("padding") or {}
        if any(k in padding for k in ("leftDp", "rightDp", "topDp", "bottomDp")):
            chain.append(
                ".padding(start={left}.dp, top={top}.dp, end={right}.dp, bottom={bottom}.dp)".format(
                    left=padding.get("leftDp", 0),
                    top=padding.get("topDp", 0),
                    right=padding.get("rightDp", 0),
                    bottom=padding.get("bottomDp", 0),
                )
            )
        token_ref = props.get("tokenRef")
        if token_ref and isinstance(token_ref, str) and token_ref.startswith("#"):
            chain.append(f".background(Color(0xFF{token_ref[1:]}))")
        x_dp = props.get("xDp")
        y_dp = props.get("yDp")
        if x_dp or y_dp:
            chain.append(f".offset(x={x_dp or 0}.dp, y={y_dp or 0}.dp)")
        content_description = props.get("contentDescription")
        if content_description:
            chain.append(f'.semantics {{ this.contentDescription = "{_escape(str(content_description))}" }}')
        test_tag = props.get("testTag")
        if test_tag:
            chain.append(f'.then(Modifier) // testTag: "{_escape(test_tag)}"')
        return "".join(chain)

